
// Java Bangla Tutorials 36 : Math Class

public class program25 {
  public static void main(String[] args) {
     System.out.println("\n");  

     int a = 20;
     int b = 10;  

     System.out.println("Maximum Value = "+Math.max(a, b));
     System.out.println("Minimum Value = "+Math.min(a, b));
     System.out.println("Abs Value = "+Math.abs(a));  
     System.out.println("Pow Value = "+Math.pow(a, b));
     System.out.println("Round Value = "+Math.round(a));
     System.out.println("Rint Value = "+Math.rint(a));
     System.out.println("Asin Value = "+Math.asin(a));
     System.out.println("Acos Value = "+Math.acos(a)); 
     System.out.println("PI = "+Math.PI);
     System.out.println("Cos Value = p"+Math.cos(a));
  }
}
