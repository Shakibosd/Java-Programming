// Java Bangla Tutorials 17 : Assignment Operator

public class program7 {
      public static void main(String[] args){
         
            System.out.println("\n");

            int x = 3;
            int y = 2;

            x+=y;
            System.out.println("X = "+x);

            x-=y;
            System.out.println("X = "+x);

            x*=y;
            System.out.println("X = "+x);

            x/=y;
            System.out.println("X = "+x);

            x%=y;
            System.out.println("X = "+x);
      }   

}




