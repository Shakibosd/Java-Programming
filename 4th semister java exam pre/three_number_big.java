public class three_number_big{
    public static void main(String[] args) {
        int a=40, b=30, c=20;
        if(a>b && a>c){
            System.out.println("A Is Big");
        }
        else if(b>a && b>c){
            System.out.println("B Is Big");
        }
        else{
            System.out.println("C Is Big");
        }
    }
}
